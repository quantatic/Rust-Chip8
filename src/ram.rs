pub struct Ram {
    ram: [u8; 0x1000],
}

impl Ram {
    pub fn new() -> Ram {
        let mut res = Ram {
            ram: [0; 0x1000],
        };

        for i in 0..80 {
            res.set(i, FONT_SET[usize::from(i)]);
        };

        res
    }

    pub fn read(&self, addr: u16) -> u8 {
        self.ram[usize::from(addr)]
    }

    pub fn set(&mut self, addr: u16, new: u8) {
        self.ram[usize::from(addr)] = new;
    }
}
